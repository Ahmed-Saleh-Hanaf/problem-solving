#include <iostream>
using namespace std;
//-----------------------------
#define el "\n"
#define ull unsigned long long
#define ll long long 
//------------------------------
int main() {
    ll num1 , num2 ;
    cin >> num1 >> num2 ;
    cout << num1 << " + " << num2 << " = " << num1 + num2 << el;
    cout << num1 << " * " << num2 << " = " << num1 * num2 << el;
    cout << num1 << " - " << num2 << " = " << num1 - num2 << el;
    return 0;
}
